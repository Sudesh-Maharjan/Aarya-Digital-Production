<p class="col-12">
        <p class="mb-4">Crowds watched solemnly as the body of Rep. John Lewis crossed the Edmund Pettus Bridge one final time, 55 years after the civil rights icon marched for peace and was met with brutality in Selma, Alabama.</p>

    <p class="mb-4"> Body bearers from the U.S. armed forces placed the late Georgia congressman and civil rights icon onto a horse-drawn caisson Sunday at the Brown Chapel African Methodist Episcopal Church. From there, the public were allowed to line up to honor Lewis for about a half-mile to the foot of the bridge.
    </p>

    <p class="mb-4">
    Rep. Terri Sewell, D-Al., thanked Lewis' family during a ceremony at the chapel for sharing the congressman with the public for so many years.


    </p>
    <p class="mb-4">
    “Our nation is better off because of John Robert Lewis,” she remarked. “My life is better, Selma is better, this nation and this world is better because of John Robert Lewis.”
    </p>
